<div id="readme_tabs-1"  style="font-size:13px; line-height: 20px;">
	This is my weekend project to help people (and myself?) troll youtube better.  It addresses my four main concerns:
	<ul>
		<li><b>No page loading:</b> right now on Youtube, every time you hit a link, it will reload the entire page. 
			Well, on FastTube, the moment you land on the site, you are done with my host and loading HTML junk.
			Everything from that moment on is just you communicating with Youtube server and retrieving NECESSARY video information via JSON and AJAX update.
			Don't know what it is? Don't worry. It's just more efficient. </li>
		<li><b>Rating info:</b> This is very important to me. There were countless time that I gave up on my Youtube trolling session because I hit a terrible/fake video.
			That would have been prevented if the rating information was displayed before hand. I already had that feature in place. However, Youtube team informed me that
			there are some known glitches with the API rating info and they are fixing it. Want to help push it faster with Youtube team?
			<a href="https://groups.google.com/forum/embed/?place=forum/youtube-api-gdata&showsearch=true&showpopout=true&parenturl=http://code.google.com/apis/youtube/forum/discussion.html?place%3Dnewtopic/youtube-api-gdata#!topic/youtube-api-gdata/u3IR24O0MMw">Click here</a>.  
			So if you see some blank rating bars in related videos section, be patient! Youtube will smoothen it out soon enough</li>
		<li><b>Hot key automation:</b> Grab a wireless keyboard and you can view your youtube videos with efficiency like a boss. 
			No more mouse clicks! Just pay attention to the red-text-inside-yellow-circle.</li> 
		<li><b>FUN:</b> It was fun messing around with jQuery UI and Youtube API. Though wish I had known up front that Youtube API is having a glitch.</li>
	</ul>
	
	I know that it lacks A LOT of actual Youtube features like channels/uploads/comments... But this is a fun one-man-team project so I would like to keep it minimal and viable. 
	Let me know if some features are extremly important to you and I will try to squeeze in some time for it.  
